    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/supplychain","root","");
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select * from billsupplier");
            int id = 0;
            int quan1 = 0;
            int quan = 0;
            while(rs.next()) {
                if(rs.getString(2).equals(jTextField1.getText()))
                {
                    id =  Integer.parseInt(rs.getString(5));
                    quan1 = Integer.parseInt(rs.getString(7));
                    String sql = " delete from billsupplier where billno = "+jTextField1.getText();
                    PreparedStatement ps = conn.prepareStatement(sql);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Bill Deleted Successfully..!!"); 
                    Statement st1 = conn.createStatement();
                    ResultSet rs1 = st1.executeQuery("Select * from products");
                    while(rs1.next()) {
                    if(rs1.getString(1).equals(String.valueOf(id)))
                    {
                        int sto = 0;
                        quan = Integer.parseInt(rs.getString(3));
                        sto = quan + quan1; 
                        String sql1 = "update products set stock = "+sto+" where productid = "+id+"  ";
                        PreparedStatement ps1 = conn.prepareStatement(sql1);
                        ps1.executeUpdate();
                        jTextField1.setText(null);
                    }
                }
                } 
            }
    }catch(SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

